def t(key, lang='pt', **fmt):
    """Retorna a tradução de `key` em `lang`, com format() opcional."""
    val = T[key][_IX.get(lang or 'pt', 0)]
    if fmt and isinstance(val, str):
        return val.format(**fmt)
    return val


def meses_label(lang='pt'):
    """Dict {1: 'Jan', ...} no idioma."""
    nomes = T['meses'][_IX.get(lang or 'pt', 0)]
    return {i + 1: nomes[i] for i in range(12)}
